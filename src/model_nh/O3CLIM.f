C
      DO KK=1,NKK
        RSTD(KK)=DUO3N(N,KK)
      ENDDO
C
      NKM=NK-1
      NKMM=NK-3
C***
C***  BESSELS HALF-POINT INTERPOLATION FORMULA
C***
