           DUO3N(N,KK)=TEMPN(N)
         ENDDO
   50   CONTINUE
      ENDIF
C
C***  DUO3N NOW IS O3 PROFILE FOR APPROPRIATE SEASON AT STD PRESSURE
C***  LEVELS
C
C***  BEGIN LATITUDE (10 DEG) LOOP
C
